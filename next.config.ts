              "connect-src 'self' https://api.stripe.com https://*.supabase.co https://raw.githack.com https://raw.githubusercontent.com",
              "frame-src 'self' https://js.stripe.com https://hooks.stripe.com",
              "media-src 'self' blob:",
              "object-src 'none'",
              "base-uri 'self'",
              "form-action 'self'",
